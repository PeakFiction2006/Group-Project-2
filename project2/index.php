            <h2 class="superheader">Our System</h2>
            <h2>&#127760; Digital Protections</h2>
            Hackers most often exploit a security breach in your systems to get an opening into your most sensitive of data.
            To prevent this we first start out with a digital sweep of your current systems to analyse any potential or current
            breaches. Then it is our porogative to patch them up and inform our clients. Moving on we require all devices to have installed
            the same digital protections on all devices with access to company data. Without giving an exhaustive list we provide: 
            firewall, anti-virus, phishing protection, encryption, etc... No device with access to company data should be allowed 
            into company servers, and that is hard coded into permissions of access for the company's server. If the company is
            assailed by hackers often, we provide monthly scans of all devices and servers issued with a complete report. Furthermore, 
            our staff will attempt penetration tests to improve security systems in a timely fashion every month. 
        
           <h2>&#128187; Device Protections</h2>
           What is the point in a strong digital protection, when your employees have the passwords to their devices written on a post it note? 
           We offer to our serious clients multifactor authentication services. Wherein each employees devices are only accessible by them and 
           and higher-up management. Ideally we further get rid off passwords to instead involve bio encryption to access the device itself. 
           Furthermore, to prevent espionage theft through internal staff, we also provide heirarchical encryption. We provide everyone with a 
           security clearance ID. Depending on how high you are in the company the more security clearances you have. And to get access to
           certain shared file servers on their device it requires them to input their ID, if they have the clearance access is provided.
        
           <h2> &#128100; People Protections</h2>
           It is often the case that despite heavy online and device securities applied leaks still occur.
           This is due to human error on the side of the company's employees. Giving away info that may seem
           innocuous but gives away enough for coorparate spies to infiltrate the company and obtain what data
           they so please. For instance if a person on the phone asks about applying to the firm over a phone
           call and asks "are there any good places for coffee?", well that gives away info of where the employees
           usually take their breaks and if their security card is on their person, there are devices small enough 
           to fit onto their hand to copy said data on the card to make a copy and access the company devices. This 
           form of data breach is highly studied in a form of hacking called "Social Engineering". At Victoria Digital
           Security we train employees against these forms of social hacking, by way of seminars & live demonstrations 
           at the most basic. If the company feels the training is yet sufficient we can offer unannounced data breach 
           tests and submit an extensive report on areas of improvement. If the company structure requires the cycling 
           of old to new workers, we offer monthly training services to ensure the most complete data security from your workers.
